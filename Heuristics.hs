module Heuristics
  ( getScore,
  )
where

import Chess
import qualified Data.Map as Map

pieceValue :: PieceType -> Int
pieceValue Pawn = 100
pieceValue Knight = 300
pieceValue Bishop = 301
pieceValue Rook = 500
pieceValue Queen = 900
pieceValue King = 0

doubledBlockedAndIsolatedPawns :: Color -> Board -> Int
doubledBlockedAndIsolatedPawns c b = 0

weightPieces :: State -> Color -> Int
weightPieces state color = sum (map (\(pos, piece) -> pieceValue (pieceType piece) + getPieceTableValue piece pos)
                                    (filter (\(pos, piece) -> pieceColor piece == color) (Map.assocs (stateBoard state))))

getScoreWhite :: State -> Int
getScoreWhite state =
  2 * (weightPieces state White - weightPieces state Black)
    - (doubledBlockedAndIsolatedPawns White (stateBoard state) - doubledBlockedAndIsolatedPawns Black (stateBoard state))

getScore :: Color -> State -> Int
getScore White = getScoreWhite
getScore Black = negate . getScoreWhite

getPieceTableValue :: Piece -> Pos -> Int
getPieceTableValue (Piece White kind) (col, row) = getPieceTable kind !! (row-1) !! (col-1)
getPieceTableValue (Piece Black kind) (col, row) = getPieceTableValue (Piece White kind) (col, 9-row)

